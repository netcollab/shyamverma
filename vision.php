<p>Art, and here we mean visual arts, is a vast, intriguing and complex concept. The visual arts are directly linked to our senses and primarily that of sight. To paint a picture or design a building the artist needs to be able to see what he is doing. But this does not mean that the creative process is bound by what can actually be seen.</p>
<p>
Every individual has his own view of the physical reality. In the same way an artist expresses his perception of the world around him. This can also be termed as the "artistic vision". It is through this vision that the artist communicates his extra awareness of the reality of things.</p>
<p>
Thus, art is both vision and communication and the language or the mode of expression of art is fluid and undergoing continual metamorphosis.
</p>

<p>
Once the vision is clear then the need arises for a medium through which the vision can be communicated. This decision is important because the medium has to be equal to the vision. In case of disparity the vision will be communicated partly or even perhaps interpreted differently than intended.
</p>

<p>
A good portrait can be broadly defined as the faithful portrayal of the character of an individual and the artist is able to depict the uniqueness of the personality of his subject. However, a great portrait passes beyond the character of the sitter to certain universal implications which represent the passions and aspirations of humanity in general.
</p>
<p>
The portraits of Shyam Verma have this universal appeal which goes beyond mere psychological portrayal of his subjects. We not only get the impression of vitality on viewing his portraits but also a sense of sublimity.</p>
